def cycle_sample_range(start, end, n_samples, shuffle=False) -> np.ndarray:
    """
    Sample n_samples values from range(start, end) such that each value appears at most once
    per cycle, and the set is reset after each cycle.

    Args:
        start (int): Start of range (inclusive)
        end (int): End of range (exclusive)
        n_samples (int): Number of samples to draw
        shuffle (bool): Whether to shuffle the samples at the end or not. If is False, the final (remainder) samples will be in order of the range. If True, they final (remainder) samples will be randomly drawn from the range without replacement.

    Returns:
        np.ndarray: 1d array of n_samples sampled integers
    """

    if n_samples <= 0:
        return np.array([])

    elements = np.arange(start, end)
    n = len(elements)

    full_cycles = n_samples // n
    remainder = n_samples % n

    samples = []
    for _ in range(full_cycles):
        samples.append(elements)

    if remainder > 0:
        if not shuffle:
            samples.append(elements[:remainder])
        else:
            samples.append(np.random.permutation(elements)[:remainder])

    samples = np.concatenate(samples)

    if shuffle:
        samples = np.random.permutation(samples)

    return samples
